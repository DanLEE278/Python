# \n 줄바꿈
print("where there is a will \nthere is a way")

# \" \': 문장 내에서 따옴표
print("나는 '이유현'입니다")
print('나는 "이유현"입니다')
print("나는 \"이유현\"입니다")
print("나는 \'이유현\'입니다")

# \\ : 문장 내에서 \
print("c\\users\\dan\\cats_vs_dogs")

# \r: 커서를 앞으로 이동
print("red apple\rpine")

# \b : 백스페이스 (한 글자 삭제)
print("Red\bApple")

# \t : 탭
print("\tHello\tworld")
